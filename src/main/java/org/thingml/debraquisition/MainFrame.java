    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (driver != null) { // this is a diconnect
            if (fileLogger != null) stopCapture();
            driver.disconnect();
            driver.clearDataListeners();
            driver = null;
            jButton1.setText(" Connect ");
            jComboBoxDevice.setEnabled(true);
            jButton2.setEnabled(true);
            jLabel2.setText("Not Connected.");
            jButton3.setEnabled(false);
        }
        else { // This is a connect
            String port = jComboBoxDevice.getSelectedItem().toString();
            driver = new SerialDriver();
            driver.addDataListener(this);
            if (driver.connect(port)) {
                jButton1.setText("Disconnect");
                jComboBoxDevice.setEnabled(false);
                jButton2.setEnabled(false);
                jLabel2.setText("Connected to device " + port);
                jButton3.setEnabled(true);
            }
            else {
                System.err.println("Error connecting to device " + port);
                jLabel2.setText("Error connecting to device " + port);
                driver.disconnect();
                driver.clearDataListeners();
                driver = null;
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

