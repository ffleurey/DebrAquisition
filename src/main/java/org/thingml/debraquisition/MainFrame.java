    FileLogger fileLogger = null;
    JFileChooser j = new JFileChooser();
    
    private void stopCapture() {
         fileLogger.close();
        driver.removeDataListener(fileLogger);
        fileLogger = null;
        jButton3.setText("Start File Capture...");
        jLabel2.setText("Capture Stopped.");
    }
    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        if (fileLogger != null) { // Stopping
           stopCapture();
        }
        else { // Start loggging
            
            j.setDialogTitle("Select Ouput Directory...");
            j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            Integer opt = j.showSaveDialog(this);
            if (opt == JFileChooser.APPROVE_OPTION) {
                try {
                    File folder = j.getSelectedFile();
                    File file = new File(folder, "Ch0_" + (new SimpleDateFormat("yyyyMMddhhmmss").format(new Date())) + ".csv");
                    fileLogger = new FileLogger(file);
                    jLabel2.setText("Capturing in " + file.getAbsolutePath());
                } catch (IOException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                    jLabel2.setText("Error creating log file !");
                    return;
                }
                driver.addDataListener(fileLogger);
                jButton3.setText("- Stop File Capture -");
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

